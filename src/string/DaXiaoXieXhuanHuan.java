package string;

public class DaXiaoXieXhuanHuan{
	public static void main(String[] args) {
		String str = "adfsfFEDVFDAgfdsDSAGdgsahJH";
		String lowerCase = str.toLowerCase();
		String upperCase = str.toUpperCase();
		System.out.println("original: "+str);
		System.out.println("low:      "+lowerCase);
		System.out.println("up:       "+upperCase);
	}

}
